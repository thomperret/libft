int	ft_atoi(const char *str)
{
	int nb;
	int p;

	p = 1;
	nb = 0;
	while (*str == '\n' || *str == '\t' || *str == ' ' || *str == '\v'
			|| *str == '\r' || *str == '\f')
		str++;
	if (*str == '-' || *str == '+')
		p = (*str++ == '+');
	while (*str <= '9' && *str >= '0')
		nb = nb * 10 + *str++ - '0';
	return (p ? nb : (-nb));
}
